#!/usr/bin/env python3
"""Expand dataset to 300+ poems by adding more poems per place."""
import json

places = json.load(open("public/data/places.json"))
existing = set()
for p in places:
    for po in p["poems"]:
        existing.add((po["title"], po["author"]))

added = 0
def add(place_id, title, author, dynasty, content):
    global added
    if (title, author) in existing:
        return
    for p in places:
        if p["id"] == place_id:
            p["poems"].append({"title": title, "author": author, "dynasty": dynasty, "content": content})
            existing.add((title, author))
            added += 1
            return

