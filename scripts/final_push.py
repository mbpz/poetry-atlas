"""Final push to 500+ poems."""
import json

places = json.load(open("public/data/places.json"))
existing = set()
for p in places:
    for po in p["poems"]:
        existing.add((po["title"], po["author"]))

added = 0

def add(pid, title, author, dynasty, content):
    global added
    if (title, author) in existing:
        return
    for p in places:
        if p["id"] == pid:
            p["poems"].append({
                "title": title, "author": author,
                "dynasty": dynasty, "content": content
            })
            existing.add((title, author))
            added += 1
            return

